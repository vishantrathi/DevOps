public class relationaloperator {
    public static void main(String args[]){
        int a=10;
        int b=20;
        System.out.println(a==b); //equal hai ya nahi
        System.out.println(a!=b); //not equal to
        System.out.println(a>b);//more than
        System.out.println(a<b);//less than
        System.out.println(a>=b);//more than equal to
        System.out.println(a<=b);//less than equal to
    }
    // Output Value is in TRUE and FLASE
    
}
